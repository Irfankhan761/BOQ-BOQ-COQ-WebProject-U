function addDataToFlickitySlider() {
    const flickitySlider = $('.js-flickity-slider');


    carouselData.forEach(item => {
        const carouselCell = $('<div class="js-carousel-cell"></div>');
        carouselCell.html(`
            <!-- Your HTML structure for each carousel cell -->
            <div class="ag-shop-card_box-wrap">
                <div class="ag-shop-card_box">
                    <div class="ag-shop-card_body">
                        <div class="js-card-bg ag-card-bg" style="background-image: url(${item.backgroundImage});"></div>
                        <a href="${item.shopLink}" class="ag-shop-card-body_link"><img src="${item.imageSrc}" alt="${item.title}" class="ag-shop-card-body_img" /></a>
                    </div>
                    <div class="ag-shop-card_footer">
                        <a href="${item.shopLink}" class="ag-shop-card-footer_link"><img src="${item.arrowSrc}" class="ag-shop-card-footer_arrow" /></a>
                        <span class="ag-shop-card-footer_title">${item.title}</span>
                        <span class="ag-shop-card-footer_products">${item.products}</span>
                    </div>
                </div>
            </div>
        `);
        flickitySlider.append(carouselCell);
    });
}


addDataToFlickitySlider();

(function ($) {
    $(function () {
      var agSlideFlickity = $(".js-flickity-slider").flickity({
    autoPlay: 2000,
      imagesLoaded: true,
      percentPosition: false,
      prevNextButtons: false,
      initialIndex: 5,
      pageDots: false,
      groupCells: 1,
      });
  
      var agCard = agSlideFlickity.find(".js-carousel-cell .js-card-bg"),
        agTransform =
          "string" == typeof document.documentElement.style.transform
            ? "transform"
            : "WebkitTransform",
        agSlide = agSlideFlickity.data("flickity");
  
      agSlideFlickity.on("scroll.flickity", function () {
        agSlide.slides.forEach(function (t, e) {
          var n = agCard[e],
            i = (-1 * (t.target + agSlide.x)) / 3;
  
          n.style[agTransform] = "translateX(" + i + "px)";
        });
      });
  
      agSlideFlickity.on("dragStart.flickity", function (t, e) {
        document.ontouchmove = function (t) {
          t.preventDefault();
        };
      });
  
      agSlideFlickity.on("dragEnd.flickity", function (t, e) {
        document.ontouchmove = function (t) {
          return true;
        };
      });
    });
  })(jQuery);
  